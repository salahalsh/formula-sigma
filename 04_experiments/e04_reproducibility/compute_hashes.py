"""Build expected_hashes.txt from current 05_results/ outputs.

Run AFTER a clean rebuild of all experiments to snapshot the
expected file hashes for downstream reproducibility verification.
"""
from __future__ import annotations
import hashlib, sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))
from common import paths


PATTERNS = ["*.csv", "*.json"]


def sha256(p: Path) -> str:
    h = hashlib.sha256()
    with open(p, "rb") as fh:
        for chunk in iter(lambda: fh.read(8192), b""):
            h.update(chunk)
    return h.hexdigest()


def main():
    rows = []
    for root in [paths.RESULTS / "e01", paths.RESULTS / "e02",
                 paths.RESULTS / "e03", paths.TABLES_RAW]:
        for pat in PATTERNS:
            for p in sorted(root.rglob(pat)):
                if "run_manifest" in p.name:
                    continue  # manifest has timestamps and is intentionally non-deterministic
                rel = p.relative_to(paths.RESULTS)
                rows.append((str(rel).replace("\\", "/"), sha256(p)))

    out = paths.EXPERIMENTS / "e04_reproducibility" / "expected_hashes.txt"
    with open(out, "w", encoding="utf-8") as fh:
        fh.write("# Generated by compute_hashes.py - re-run after deterministic rebuild.\n")
        fh.write("# Format: <sha256>  <relative_path_under_05_results>\n")
        for path, h in rows:
            fh.write(f"{h}  {path}\n")

    print(f"Wrote {len(rows)} hashes -> {out}")


if __name__ == "__main__":
    main()
